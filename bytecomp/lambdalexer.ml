let remove_underscores s =
  let l = pred (String.length s) and c = ref 0 in
  for i = 0 to  l do
    if s.[i] != '_' then incr c
  done;
  let s' = String.make !c ' ' and c = ref 0 in
  for i = 0 to l do
    if s.[i] != '_' then
    begin 
      s'.[!c] <- s.[i];
      incr c
    end
  done;
  s'

